# Repetição de Números em incremento até 10 unidades
# O código acima solicita ao usuário que digite um número e, em seguida, imprime esse número 10 vezes, incrementando o número a cada iteração.
numero = int(input("Digite um número: "))
i = 1
while i <= 10:
    print(numero)
    i += 1
    numero += 1

# Tabuada com o código para imprimir os números de 1 a 10: multiplicando o número por i
numero = int(input("Digite um número: "))
i = 1
while i <= 10:
    print(numero * i)
    i += 1

